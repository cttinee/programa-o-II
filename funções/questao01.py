# Defina uma função chamada velocidade_media() em um script chamado funcoes.py que recebe dois parâmetros: a distância percorrida (em metros) e o tempo
# (em segundos) gasto.
# Entrada; 100, 20
# Saída: 5

def velocidade_media(distancia, tempo):
    velocidade = distancia / tempo 
    print(velocidade)
velocidade_media(100, 20)